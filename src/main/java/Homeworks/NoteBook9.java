package Homeworks;

public class NoteBook9 {
    private String name;
    private String model;
    private int year;

    public NoteBook9(String name, String model, int year){
        this.name = name;
        this.model = model;
        this.year = year;
    }
    public String getName(){
        return name;
    }
    public String getModel(){
        return model;
    }

    public int getYear() {
        return year;
    }
}
